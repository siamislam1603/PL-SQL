-- VOID FUNCTION WITH PARAMETER
CREATE OR REPLACE PROCEDURE printID(A IN MONEY.ID%TYPE)
IS

BEGIN
	DBMS_OUTPUT.PUT_LINE(A);
END printID;
/

BEGIN
	printID(2);
END;
/

--ANOTHER WAY OF PROCEDURE CALL
EXEC printID(4); 

-- RETURN FUNCTION WITH ACCEPTING VALUES IN PARAMETER
CREATE OR REPLACE PROCEDURE isEven(A IN MONEY.TAKA%TYPE,B OUT MONEY.ID%TYPE)
IS

BEGIN
	IF MOD(A,2)=0 THEN
		DBMS_OUTPUT.PUT_LINE(A || ' IS EVEN');
	ELSE
		DBMS_OUTPUT.PUT_LINE(A || ' IS ODD');
	END IF;
	SELECT MAX(ID) INTO B FROM MONEY;
END isEven;
/

DECLARE
	MAX_ID int;
BEGIN
	isEven(203,MAX_ID);
	DBMS_OUTPUT.PUT_LINE('MAX ID FROM MONEY IS = '||MAX_ID);
END;
/

