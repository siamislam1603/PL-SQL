-- RETURN FUNCTION WITH ACCEPTING VALUES IN PARAMETER
CREATE OR REPLACE PROCEDURE F2(A IN OUT MONEY.TAKA%TYPE)
IS

BEGIN
	IF MOD(A,2)=0 THEN
		DBMS_OUTPUT.PUT_LINE(A || ' IS EVEN');
	ELSE
		DBMS_OUTPUT.PUT_LINE(A || ' IS ODD');
	END IF;
	A:=&X;
END F2;
/

DECLARE
	VAR_MONEY MONEY.TAKA%TYPE;
BEGIN
	VAR_MONEY:=220;
	F2(VAR_MONEY);
	DBMS_OUTPUT.PUT_LINE('MONEY = '||VAR_MONEY);
END;
