-- VOID FUNCTION WITHOUT PARAMETER
CREATE OR REPLACE PROCEDURE f1
IS

BEGIN
	DBMS_OUTPUT.PUT_LINE('VOID FUNCTION WITHOUT PARAMETER');
END f1;
/

BEGIN
	f1;
END;
/
--ANOTHER WAY OF PROCEDURE CALL
